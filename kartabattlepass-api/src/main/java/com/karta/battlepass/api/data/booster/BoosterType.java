package com.karta.battlepass.api.data.booster;

/** The type of benefit a booster provides. */
public enum BoosterType {
    /** Multiplies the amount of points gained from quests. */
    POINTS,
    /** Multiplies the progress gained for quest objectives. */
    PROGRESS,
    /** Multiplies the quantity of rewards received (if applicable). */
    REWARD
}
